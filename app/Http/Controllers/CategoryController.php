    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $category = Category::get();
        return view('admin.dashboard.category.category')->withCategories($category);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, array(
            'category'=>"required|max:15|unique:categories,category",
        ));
        $category = new Category;
        $category->category = $request->category;
        $category->save();

        Toastr::success('Category successfully Added', $title = 'Category', $options = ["progressBar"=>true]);

        return redirect()->back();
    }

